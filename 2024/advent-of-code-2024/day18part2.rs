const MOVES: [(isize, isize); 4] = [(1, 0), (-1, 0), (0, 1), (0, -1)];

fn solve() -> String {
    // let (n, m) = (7, 7);
    let (n, m) = (71, 71);
    let mut a = vec![vec![false; m]; n];
    loop {
        let s = readln_str();
        let s = s.trim_end();
        let co = s
            .split(',')
            .map(|s| s.parse::<usize>().unwrap())
            .collect_vec();
        a[co[1]][co[0]] = true;

        if !ok(&a) {
            return s.to_string();
        }
    }
}

fn ok(a: &Vec<Vec<bool>>) -> bool {
    let n = a.len();
    let m = a[0].len();
    let inf = n * m + 1;
    let mut dist = vec![vec![inf; m]; n];
    let mut q = VecDeque::new();
    q.push_back((0usize, 0usize));
    dist[0][0] = 0;

    while let Some((i, j)) = q.pop_front() {
        for mo in MOVES {
            let ii = i.wrapping_add_signed(mo.0);
            let jj = j.wrapping_add_signed(mo.1);

            if ii < n && jj < m && !a[ii][jj] && dist[ii][jj] > dist[i][j] + 1 {
                q.push_back((ii, jj));
                dist[ii][jj] = dist[i][j] + 1;
            }
        }
    }

    dist[n - 1][m - 1] < inf
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", res);
}

